    private boolean validarCampos() {
    if (jTextField1.getText().trim().isEmpty() ||
        jTextField2.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() ||
        jTextField4.getText().trim().isEmpty() ||
        jTextField5Telefono.getText().trim().isEmpty()) {
        
        JOptionPane.showMessageDialog(this, "Complete todos los campos", 
                "validacion", JOptionPane.WARNING_MESSAGE);
        return false;
    }

    try {
        Integer.parseInt(jTextField1.getText().trim()); // DNI
        Long.parseLong(jTextField5Telefono.getText().trim()); // Tel√©fono
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingresar solo numeros en DNI y telefono", 
                "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    return true;
}


    


    private void jButton1GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1GuardarActionPerformed
           if (!validarCampos()) return;

    int dni = Integer.parseInt(jTextField1.getText().trim());
    String nombre = jTextField2.getText().trim();
    String apellido = jTextField3.getText().trim();
    String domicilio = jTextField4.getText().trim();
    String ciudad = (String) jComboBox1Ciudad.getSelectedItem();
    long telefono = Long.parseLong(jTextField5Telefono.getText().trim());

    Cliente nuevo = new Cliente(dni, nombre, apellido, ciudad, domicilio);

    frmMenuPrincipal.directorio.put(telefono, nuevo);

    JOptionPane.showMessageDialog(this, 
            "Se agrego correctamente el cliente en el directorio", 
            "Exito", JOptionPane.INFORMATION_MESSAGE);

    limpiarCampos();
    }//GEN-LAST:event_jButton1GuardarActionPerformed

